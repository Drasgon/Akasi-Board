<div class="rules">
	<h3 class="content_warning">
		Ein ARK ohne Regeln? Nicht mit uns.
	</h3>
	<br>
	<p>
		Damit ein friedliches und gemütliches Miteinander auf unserem Server gewährleistet ist, hat sich JEDER an die folgenden Regeln zu halten, dessen Zweck genau dies ist.
	</p>
	<br>
	<br>
	<br>
		<span>
			§1 Obszöne, beleidigende oder gar explizit sexuelle Ausdrücke sind in jeglicher Kommunikationsform untersagt. Darüber hinaus ist ein freundlicher Umgangston einzuhalten.
		</span>
	<br><br>
		<span>
			§2 Den Anweisungen der Administration ist folge zu leisten.
		</span>
	<br><br>
		<span>
			§3 Das Bauen vor Höhlen oder auf Bergen ist untersagt. Höhlen müssen für jeden zugänglich sein, ungeachtet dessen, wie sich der Spieler fortbewegt.
		</span>
	<br><br>
		<span>
			§4 Offline Raiding oder das Ausbeuten von Offline Spielern, sowie das Töten derer Tiere ist ohne Erlaubnis des Betroffenen strengstens untersagt und führt zu sofortigem Spielausschluss.
		</span>
	<br><br>
		<span>
			§5 Die Erstattung von durch Serverproblemen verlorenen Gegenständen / Tieren liegt nicht im Aufgabenbereich der Administration. Dies geschieht rein aus Kulanz.
		</span>
	<br><br>
		<span>
			§6 PvE Spieler / Tribes dürfen während der PvP Zeit nicht getötet oder ausgebeutet werden. Gleiches gilt für deren Tiere und Strukturen. Es sei denn, es liegt ein ersichtlicher Provokationsgrund vor. Dies geschieht in etwa durch aufdringliches Bauen, Blockade durch Strukturen / Tiere etc.
		</span>
	<br><br>
		<span>
			§7 Paragraph 6 wird insoweit aufgehoben, dass sich trotz der Regelung betroffene Spieler wehren können, ungeachtet des PvP/PvE Status.
		</span>
	<br><br>
		<span>
			§8 Ungenutzte Strukturen (darunter auch Taming-Boxen) müssen nach Gebrauch wieder entfernt werden, um auch weiterhin eine kompromisslose Spielerfahrung bieten zu können.
		</span>
	<br><br>
		<span>
			§9 Der Administration ist es vorbehalten, diese Regeln jederzeit unter Serverweitiger Benachrichtigung per "Nachricht des Tages" zu verändern und anzupassen.
		</span>
	<br><br>
		<span>
			§10 Spielern / Tribes ist es erlaubt, sich als PvE oder PvP zu markieren. Der PvP Status hebt die Paragraphen §4, §6 und §7 für diese Spieler auf.
		</span>
	<br><br>
		<span>
			§11 Spieler ohne die in §10 genannte Markierung (darunter auch neue Spieler) sind automatisch als PvE Spieler markiert. Die Paragraphen §4, §6 und §7 sind für diese somit voll wirksam.
		</span>
	<br><br>
		<span>
			§12 Es ist untersagt, Gemeinschaftsgebäude, wie etwa die öffentliche Schmelze auf dem Vulkan, zu zerstören, oder anderweitig zu beschädigen.
		</span>
</div>